USE QLBANSACH;

CREATE TABLE ROLE 
(
    ID INT AUTO_INCREMENT,
    ROLE_NAME VARCHAR(50),
    PRIMARY KEY (ID) 
);

CREATE TABLE USER
(
    USERNAME VARCHAR(30),
    PASSWORD VARCHAR(300),
    USER_ID INT,
    ROLE_ID INT,
    PRIMARY KEY (USERNAME)
);

CREATE TABLE USER_INFOR
(
    ID INT AUTO_INCREMENT,
    USERNAME VARCHAR(30),
    FULLNAME NVARCHAR(70),
    BIRTHDAY DATETIME,
    PHONE_NUMBER VARCHAR(10),
    MAIL VARCHAR(50),
    ADDRESS NVARCHAR(100),
    CREATE_DATE DATETIME,
    UPDATE_DATE DATETIME,
    PRIMARY KEY(ID)
);

CREATE TABLE AUTHOR
(
    ID INT AUTO_INCREMENT,
    AUTHOR_NAME NVARCHAR(70),
    PRIMARY KEY (ID)
);

CREATE TABLE CATEGORY
(
    ID INT AUTO_INCREMENT,
    CATEGORY_NAME NVARCHAR(30),
    PRIMARY KEY (ID)
);

CREATE TABLE PRODUCT
(
    ID INT AUTO_INCREMENT,
    PRODUCT_NAME NVARCHAR(100),
    AUTHOR_ID INT,
    CATEGORY_ID INT,
    QUANTITY INT,
    PUBLISHED_DATE DATETIME,
    DESCRIPTION TEXT,
    IMAGE_URL TEXT,
    PRIMARY KEY (ID)
);

CREATE TABLE CART
(
    USER_ID INT,
    TOTAL_MONEY INT,
    STATE VARCHAR(30),
    PRIMARY KEY (USER_ID)
);

CREATE TABLE CART_DETAIL 
(
    ID INT AUTO_INCREMENT,
    USER_ID INT,
    PRODUCT_ID INT,
    NUMBER INT,
    PRICE INT,
    PRIMARY KEY (ID)
);

